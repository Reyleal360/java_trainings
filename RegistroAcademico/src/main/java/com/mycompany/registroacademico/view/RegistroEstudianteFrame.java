    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
  
String nombre = txtNombre.getText();
int edad = Integer.parseInt(txtEdad.getText());
double nota1 = Double.parseDouble(txtNota1.getText());
double nota2 = Double.parseDouble(txtNota2.getText());
double nota3 = Double.parseDouble(txtNota3.getText());

estudianteManager.agregarEstudiante(nombre, edad, nota1, nota2, nota3);

// ahora sí, actualizas la tabla:
Estudiante e = estudianteManager.getEstudiantes().get(estudianteManager.getEstudiantes().size() - 1);

double mayor = Math.max(e.getNota1(), Math.max(e.getNota2(), e.getNota3()));
double menor = Math.min(e.getNota1(), Math.min(e.getNota2(), e.getNota3()));
double promedio = e.calcularPromedio();
String estado = (promedio >= 3.0) ? "Aprobado" : "Reprobado";

DefaultTableModel model = (DefaultTableModel) tblEstudiantes.getModel();
model.addRow(new Object[]{model.getRowCount() + 1, e.getNombre(), mayor, menor, promedio, estado}); 
        
        
EstudianteManager.guardarTablaEnCSV(tblEstudiantes, "estudiantes.csv");
JOptionPane.showMessageDialog(this, "Datos guardados en estudiantes.csv ✅");
        
        
        


















        
    }//GEN-LAST:event_jButton2ActionPerformed

