    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
    String nombre = txtNombre.getText();
    int edad = Integer.parseInt(txtEdad.getText());
    double nota1 = Double.parseDouble(txtNota1.getText());
    double nota2 = Double.parseDouble(txtNota2.getText());
    double nota3 = Double.parseDouble(txtNota3.getText());

    Estudiante estudiante = new Estudiante(contadorId, nombre, edad, nota1, nota2, nota3);

    listaEstudiantes.add(estudiante); // guardar en memoria

    DefaultTableModel model = (DefaultTableModel) tblEstudiantes.getModel();
    model.addRow(new Object[]{
        estudiante.getId(),
        estudiante.getNombre(),
        estudiante.getNotaMayor(),
        estudiante.getNotaMenor(),
        estudiante.calcularPromedio(),
        estudiante.getEstado()
    });

    contadorId++;
  }//GEN-LAST:event_CalcularActionPerformed

